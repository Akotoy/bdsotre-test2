const supabaseUrl = process.env.VITE_SUPABASE_URL;
const supabaseKey = process.env.VITE_SUPABASE_ANON_KEY;
// NOTE: For a real seed script, you should use the SERVICE_ROLE_KEY to bypass RLS,
// but since we enabled RLS and stubbed admin access, or if we just want to insert 
// using anon key (if policies allow), this might fail if RLS blocks INSERT.
// Let's assume we temporarily disable RLS or have a service key, but for now we use anon key.

if (!supabaseUrl || !supabaseKey) {
    console.error("Missing VITE_SUPABASE_URL or VITE_SUPABASE_ANON_KEY in .env");
    process.exit(1);
}

// We will use the anon key. If RLS blocks this, you need to use the service role key.
const supabase = createClient(supabaseUrl, supabaseKey);

async function seed() {
    console.log("Starting database seed...");

    // 1. Seed Categories
    const categoriesToInsert = [
        { id: 'outerwear', title: 'Верхняя одежда' },
        { id: 'hoodies_sweatshirts', title: 'Худи и свитшоты' },
        { id: 'sweaters_jumpers', title: 'Свитеры и джемперы' },
        { id: 'tshirts_polos', title: 'Футболки и поло' },
        { id: 'shirts', title: 'Рубашки' },
        { id: 'jeans', title: 'Джинсы' },
        { id: 'trousers', title: 'Брюки' },
        { id: 'suits', title: 'Деловые костюмы' },
        { id: 'sportswear', title: 'Спортивные костюмы' },
        { id: 'shorts', title: 'Шорты' },
        { id: 'underwear', title: 'Нижнее белье' },
        { id: 'dresses', title: 'Платья' },
        { id: 'skirts', title: 'Юбки' },
        { id: 'accessories', title: 'Аксессуары' },
        { id: 'shoes', title: 'Обувь' }
    ];

    console.log("Seeding categories...");
    const { error: catError } = await supabase
        .from('bdstore_categories')
        .upsert(categoriesToInsert, { onConflict: 'id' });

    if (catError) {
        console.error("Error inserting categories:", catError);
        return;
    }

    // 2. Seed Products
    console.log("Seeding products...");
    for (const p of localProducts) {
        // Upsert Product
        const productData = {
            slug: p.article.toLowerCase(),
            title: p.title,
            price: p.price,
            old_price: p.old_price,
            description: p.description,
            details: [
                `Бренд: ${p.brand}`,
                `Сезон: ${p.season}`,
                `Состав: ${p.composition}`,
                `Цвет: ${p.color}`,
                `Пол: ${p.gender}`
            ],
            category_id: p.categoryId,
        };

        const { data: newProduct, error: pError } = await supabase
            .from('bdstore_products')
            .upsert(productData, { onConflict: 'slug' })
            .select()
            .single();

        if (pError) {
            console.error(`Error inserting product ${p.article}:`, pError);
            continue;
        }

        const productId = newProduct.id;

        // Seed Images
        if (p.images && p.images.length > 0) {
            // First delete existing images to avoid duplicates on re-seed
            await supabase.from('bdstore_product_images').delete().eq('product_id', productId);

            const imagesToInsert = p.images.map((url, index) => ({
                product_id: productId,
                url: url,
                display_order: index
            }));

            await supabase.from('bdstore_product_images').insert(imagesToInsert);
        }

        // Seed Inventory (Sizes)
        if (p.sizes && p.sizes.length > 0) {
            const inventoryToInsert = p.sizes.map(size => ({
                product_id: productId,
                size: size,
                stock_count: Math.floor(Math.random() * 11) + 5 // Random stock between 5 and 15
            }));

            // Upsert to avoid constraint errors
            await supabase.from('bdstore_inventory').upsert(inventoryToInsert, { onConflict: 'product_id, size' });
        }

        // Seed Mock Reviews
        // We had mock reviews generated in productStore.ts, let's insert a couple
        const reviewsToInsert = [
            {
                product_id: productId,
                author_name: "Анна С.",
                rating: 5,
                text: "Отличный костюм! Качество на высоте, швы ровные. Ткань приятная к телу, не просвечивает.",
                seller_reply: "Здравствуйте, Анна! Спасибо за ваш теплый отзыв и высокую оценку."
            },
            {
                product_id: productId,
                author_name: "Елена В.",
                rating: 4,
                text: "Хороший товар, но немного маломерит. Взяла на размер больше - село идеально.",
                seller_reply: null
            }
        ];

        // Clear old reviews to avoid duplicates
        await supabase.from('bdstore_reviews').delete().eq('product_id', productId);
        await supabase.from('bdstore_reviews').insert(reviewsToInsert);
    }

    console.log("Database seed completed successfully.");
}

seed();
